<?php

namespace phpweb\Framework\Build;

use phpweb\ProjectGlobals;
use RuntimeException;
use stdClass;
use function dirname;
use function is_dir;
use function is_readable;

/**
 * Pregenerated Data Helper for storing and retrieving data from the compiled cache
 *
 * This utility is used by various build steps.
 *
 * At present these steps must be performed, and the cache committed to the GIT repository.
 * It is hoped that in the long-term this can be performed as part of CI/CD.
 */
final class VarCache
{
    public static function ReadVar(string $key): mixed
    {
        static $dummy = null;
        $dummy ??= new stdClass();
        $value = self::ReadVarWithDefault($key, $dummy);

        if ($value === $dummy) {
            throw new VarUnavailableException('Unable to read var cache value for ' . $key);
        }

        return $value;
    }

    public static function ReadVarWithDefault(string $key, mixed $default): mixed
    {
        $path = self::makePathForKey($key);
        if (is_readable($path)) {
            return require $path;
        }

        return $default;
    }

    public static function WriteVar(string $key, mixed $value): void
    {
        $path = self::makePathForKey($key, create: true);
        $result = file_put_contents(
            $path,
            "<?php\n\n/* This file is automatically generated. Do not edit. */\nreturn " . var_export($value, true) . ';',
        );

        if ($result === false) {
            throw new RuntimeException('Unable to write var cache value for ' . $key);
        }
    }

    private static function makePathForKey(string $key, bool $create = false): string
    {
        $path = ProjectGlobals::getProjectRoot() . "/var/cache/{$key}.inc";

        if ($create) {
            $basePath = dirname($path);
            is_dir($basePath) || mkdir($basePath, recursive:true);
        }

        return $path;
    }
}
